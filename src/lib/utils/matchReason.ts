export function getMatchReason(job: any, score: number): string {
  const title = job.title ?? "";
  const jobType = job.job_category ?? "";

  // 1. タイトル完全一致を優先
  const titleReasons = TITLE_REASONS[title];
  if (titleReasons) {
    const idx = Math.floor(score / 10) % titleReasons.length;
    return titleReasons[idx] + getPhaseBonus(job);
  }

  // 2. タイトル部分一致
  for (const [key, reasons] of Object.entries(TITLE_REASONS)) {
    if (title.includes(key) || key.includes(title)) {
      const idx = Math.floor(score / 10) % reasons.length;
      return reasons[idx] + getPhaseBonus(job);
    }
  }

  // 3. カテゴリ一致
  const categoryKey = Object.keys(CATEGORY_REASONS).find((r) => jobType.includes(r));
  const baseReasons = CATEGORY_REASONS[categoryKey ?? "営業"] ?? CATEGORY_REASONS["営業"];
  const idx = Math.floor(score / 10) % baseReasons.length;
  return baseReasons[idx] + getPhaseBonus(job);
}

function getPhaseBonus(job: any): string {
  const phase = job.ow_companies?.phase ?? "";
  return PHASE_SUFFIX[phase] ?? "";
}
