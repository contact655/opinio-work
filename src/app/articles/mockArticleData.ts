export function getArticleBySlug(slug: string): Article | undefined {
  return MOCK_ARTICLES.find((a) => a.slug === slug);
}

export function filterArticles(
  articles: Article[],
  params: { type?: string; sort?: string }
): Article[] {
  let result = [...articles];
  if (params.type && params.type !== "all") {
    result = result.filter((a) => a.type === params.type);
  }
  if (params.sort === "popular") {
    result = result.sort((a, b) => b.read_min - a.read_min);
  } else {
    result = result.sort((a, b) => (a.date < b.date ? 1 : -1));
  }
  return result;
}

